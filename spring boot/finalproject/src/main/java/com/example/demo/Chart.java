package com.example.demo;

public class Chart {
	String totalEarning;
	String usbangla;
	String novoair;
	String bimanbangladesh;
	public Chart() {
		super();
	}
	public Chart(String totalEarning, String usbangla, String novoair, String bimanbangladesh) {
		super();
		this.totalEarning = totalEarning;
		this.usbangla = usbangla;
		this.novoair = novoair;
		this.bimanbangladesh = bimanbangladesh;
	}
	public String getTotalEarning() {
		return totalEarning;
	}
	public void setTotalEarning(String totalEarning) {
		this.totalEarning = totalEarning;
	}
	public String getUsbangla() {
		return usbangla;
	}
	public void setUsbangla(String usbangla) {
		this.usbangla = usbangla;
	}
	public String getNovoair() {
		return novoair;
	}
	public void setNovoair(String novoair) {
		this.novoair = novoair;
	}
	public String getBimanbangladesh() {
		return bimanbangladesh;
	}
	public void setBimanbangladesh(String bimanbangladesh) {
		this.bimanbangladesh = bimanbangladesh;
	}

	
	

}
